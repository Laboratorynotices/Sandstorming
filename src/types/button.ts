/**
 * Типы и интерфейсы для компонента кнопки `AppButton`.
 */

import { ButtonVariant } from "../constants/button";

/**
 * Возможные HTML-теги, которые может использовать компонент кнопки.
 *
 * - `'button'` — стандартная кнопка.
 * - `'a'` — ссылка.
 * - `'span'` — элемент без действий, используется для стилизации.
 */
export type ButtonTag = "button" | "a" | "span";

/**
 * Свойства компонента `AppButton`, определяющие его поведение и внешний вид.
 */
export interface AppButtonProps {
  /**
   * Вариант отображения кнопки (визуальный стиль).
   *
   * Используются значения из enum `ButtonVariant`, например:
   * - `Call` — кнопка вызова.
   * - `Header1`, `Header2` — кнопки для заголовков.
   * - `Event1`, `Event2` — кнопки для мероприятий.
   */
  variant: ButtonVariant;

  /**
   * HTML-тег, который будет использован для рендера кнопки.
   *
   * По умолчанию выбирается автоматически:
   * - `'a'`, если задан `href`.
   * - `'span'`, если `href` не задан.
   */
  as?: ButtonTag;

  /**
   * Адрес ссылки, если кнопка должна вести куда-либо.
   *
   * При наличии `href` компонент будет использовать тег `<a>`.
   */
  href?: string;
}
